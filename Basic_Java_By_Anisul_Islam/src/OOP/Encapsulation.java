package OOP;

public class Encapsulation {
    //hide data type
    private String name;
    private int age;
    
    //create setter and getter method 
    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
    
    
    
    
}
